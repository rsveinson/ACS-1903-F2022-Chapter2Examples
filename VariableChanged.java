/**
 * This Java class declares 
 * an int variable named i,
 * assigns it a value and then
 * changes its value.
 */
public class VariableChanged
{
    public static void main(String[] args){
        int i;
        i = 14;
        System.out.println(i);
        i = 30;
        System.out.println(i);
    }
}
